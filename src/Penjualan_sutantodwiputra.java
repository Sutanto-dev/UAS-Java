    private void BtnHitung_sutantodwiputraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnHitung_sutantodwiputraActionPerformed
        // TODO add your handling code here:
                Double JumlahBayar = (Double.parseDouble(HargaBrg_sutantodwiputra.getText()))*(Double.parseDouble(JmlBeli_sutantodwiputra.getText()));
        JmlBayar_sutantodwiputra.setText(Double.toString(JumlahBayar));
        
        if(JumlahBayar >= 200000){
            Double DiskonHarga = (Double.parseDouble(JmlBayar_sutantodwiputra.getText()))* 0.3;
            Diskon_sutantodwiputra.setText(Double.toString(DiskonHarga));      
        }
        else if(JumlahBayar >= 100000){
            Double DiskonHarga = (Double.parseDouble(JmlBayar_sutantodwiputra.getText()))* 0.2;
            Diskon_sutantodwiputra.setText(Double.toString(DiskonHarga));
        }
        else if(JumlahBayar <= 100000){
            Diskon_sutantodwiputra.setText("0");
        }
        
        Double TotalBayar = (Double.parseDouble(JmlBayar_sutantodwiputra.getText()))-(Double.parseDouble(Diskon_sutantodwiputra.getText()));
        TotalByr_sutantodwiputra.setText(Double.toString(TotalBayar));
    }//GEN-LAST:event_BtnHitung_sutantodwiputraActionPerformed
    
