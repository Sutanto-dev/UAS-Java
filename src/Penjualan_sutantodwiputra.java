    private void tampil_data1(){
        tabel1.getDataVector().removeAllElements();
        tabel1.fireTableDataChanged();
        try {
        String query = "SELECT * FROM DATA_PENJUALAN ORDER BY id_transaksi DESC";
        ps = c.prepareStatement(query);
        r = ps.executeQuery();
        while (r.next()) {
        Object obj[] = new Object[9];
        obj[0] = r.getString(1);
        obj[1] = r.getString(2);
        obj[2] = r.getString(3);
        obj[3] = r.getString(4);
        obj[4] = r.getString(5);
        obj[5] = r.getString(6);
        obj[6] = r.getString(7);
        obj[7] = r.getString(8);
        tabel1.addRow(obj);
        }
        } catch (SQLException e) {
            System.out.println(e);
        }
    }
